def test_an_unrelated_cohort_does_not_alter_another_cohorts_result(scenario: dict) -> None:
    """P0.4A isolation, verified inside the causal chain rather than in the abstract."""
    base = run(scenario)
    extended_scenario = json.loads(json.dumps(scenario))
    extra = json.loads(json.dumps(extended_scenario["cohorts"][0]))
    extra["cohort_id"] = "newly-added-unrelated-cohort"
    extra["network_position"]["bridges_to"] = []
    extended_scenario["cohorts"].append(extra)
    extended = run(extended_scenario)

    for cid in base.state.cohorts:
        assert extended.state.cohorts[cid].economic_concern == pytest.approx(
            base.state.cohorts[cid].economic_concern, abs=1e-12
        ), f"adding an unrelated cohort moved {cid}"


def test_cohort_evaluation_order_does_not_change_the_outcome(scenario: dict) -> None:
    reordered = json.loads(json.dumps(scenario))
    reordered["cohorts"] = list(reversed(reordered["cohorts"]))
    a, b = run(scenario), run(reordered)
    assert a.state.chain.household_expectation_pressure == pytest.approx(
        b.state.chain.household_expectation_pressure, abs=1e-12
    )
    for cid in a.state.cohorts:
        assert a.state.cohorts[cid].economic_concern == pytest.approx(
            b.state.cohorts[cid].economic_concern, abs=1e-12
        )


