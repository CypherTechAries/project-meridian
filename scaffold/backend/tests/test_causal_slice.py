# --------------------------------------------------------------------------- #
# 11. Counterfactual: disabling a link removes its downstream effect
# --------------------------------------------------------------------------- #
def test_disabling_the_insurer_mechanism_removes_everything_downstream(scenario: dict) -> None:
    """The headline counterfactual. Attribution, not correlation."""
    full = run(scenario)
    without = run(scenario, enabled=[m.id for m in CHAIN if m.id != M_INSURER.id])

    assert full.state.chain.insurer_risk > 0.0
    assert without.state.chain.insurer_risk == 0.0
    # Everything downstream of the insurer collapses with it.
    for field in ("premium_pressure", "rerouting_level", "port_activity_deficit",
                  "employment_pressure", "household_expectation_pressure",
                  "narrative_attention", "collective_activity", "political_pressure"):
        assert getattr(without.state.chain, field) == 0.0, f"{field} survived insurer removal"
    # And the incident itself is unaffected — the link removed is downstream of it.
    assert without.state.chain.incident_severity > 0.0


def test_disabling_rerouting_severs_the_chain_at_that_point(scenario: dict) -> None:
    """Upstream survives, downstream dies — proving where the link sits."""
    without = run(scenario, enabled=[m.id for m in CHAIN if m.id != M_REROUTE.id])
    c = without.state.chain
    assert c.insurer_risk > 0.0, "upstream of the removed link must be unaffected"
    assert c.premium_pressure > 0.0, "upstream of the removed link must be unaffected"
    assert c.rerouting_level == 0.0
    assert c.port_activity_deficit == 0.0
    assert c.employment_pressure == 0.0
    assert c.political_pressure == 0.0


def test_disabling_household_stage_severs_cohorts_from_politics(scenario: dict) -> None:
    without = run(scenario, enabled=[m.id for m in CHAIN if m.id != M_HOUSEHOLD.id])
    c = without.state.chain
