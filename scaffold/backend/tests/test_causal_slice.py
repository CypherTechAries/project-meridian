# 16. No model or network call on the authoritative path
# --------------------------------------------------------------------------- #
def test_no_llm_or_network_call_in_the_chain(scenario: dict, monkeypatch) -> None:
    """Fail loudly if any mechanism reaches for a socket or the gateway."""
    import socket

    def forbidden(*a, **k):  # pragma: no cover - must never run
        raise AssertionError("the P0.5 authoritative path attempted a network connection")

    monkeypatch.setattr(socket.socket, "connect", forbidden)
    monkeypatch.setattr(socket.socket, "connect_ex", forbidden)

    calls: list[str] = []
    import app.simulation.llm_gateway as gw
    original = gw.propose_action
    monkeypatch.setattr(gw, "propose_action", lambda *a, **k: (calls.append("x"), original(*a, **k))[1])

    m = MeridianModel(scenario=scenario, seed=88213)
    before = len(calls)
    m.chain.run_tick()  # the chain alone, not the whole engine step
    assert len(calls) == before, "a chain mechanism called the LLM gateway"


