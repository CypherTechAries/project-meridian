def test_population_weighting_is_actually_applied(scenario: dict) -> None:
    """`represents_population` was declared but read by NO code before P0.5.

    The aggregate must equal the population-weighted mean of per-cohort concern.
    """
    m = run(scenario)
    total = sum(c.represents_population for c in m.state.cohorts.values())
    expected = sum(c.economic_concern * c.represents_population for c in m.state.cohorts.values()) / total
    assert m.state.chain.household_expectation_pressure == pytest.approx(expected, abs=1e-9)


def test_population_weighting_materially_changes_the_aggregate(scenario: dict) -> None:
    """Inflating one cohort's population must move the society-wide aggregate."""
    base = run(scenario)
    skewed_scenario = json.loads(json.dumps(scenario))
    # Make the SMALL, HIGHLY-exposed fishing cohort the demographic majority.
    for c in skewed_scenario["cohorts"]:
        if c["cohort_id"] == "coastal-creole-fishing":
            c["represents_population"] = 5_000_000
    skewed = run(skewed_scenario)

    assert skewed.state.chain.household_expectation_pressure > base.state.chain.household_expectation_pressure, (
        "making the most-exposed cohort the majority must raise society-wide pressure"
    )


def test_cohort_intensity_is_independent_of_cohort_size(scenario: dict) -> None:
    """Size affects AGGREGATE MAGNITUDE ONLY — never an individual cohort's intensity.

    The fishing cohort is the smallest and the most exposed. Its own concern must be the highest,
    regardless of how few people it represents.
    """
    m = run(scenario)
    concerns = {cid: c.economic_concern for cid, c in m.state.cohorts.items()}
    populations = {cid: c.represents_population for cid, c in m.state.cohorts.items()}
    fishing = "coastal-creole-fishing"
    assert populations[fishing] == min(populations.values()), "fixture assumption changed"
    assert concerns[fishing] == max(concerns.values()), "smallest cohort must still be most affected"


def test_cohort_concern_tracks_declared_exposure_not_population(scenario: dict) -> None:
    m = run(scenario)
    ordered_by_sensitivity = sorted(
        m.state.cohorts.values(), key=lambda c: c.income_sensitivity, reverse=True
    )
    concerns = [c.economic_concern for c in ordered_by_sensitivity]
    assert concerns == sorted(concerns, reverse=True), "concern must rank by exposure, not size"


