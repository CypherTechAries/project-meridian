def test_all_chain_mutations_pass_through_the_transition_service(scenario: dict) -> None:
    m = run(scenario, ticks=5)
    chain_types = {
        TransitionType.SET_CHAIN_SCALAR, TransitionType.SET_COHORT_CONCERN,
        TransitionType.SET_OPTION_STATUS, TransitionType.APPLY_INCIDENT,
        TransitionType.SNAPSHOT_CHAIN_PREVIOUS,
    }
    chain_records = [r for r in m.transition_log if r.type in chain_types]
    assert chain_records
    for r in chain_records:
        if r.applied and r.type is not TransitionType.SNAPSHOT_CHAIN_PREVIOUS:
            assert r.mechanism, f"{r.type} applied without mechanism attribution"
            assert r.mechanism_version, f"{r.type} applied without a mechanism version"


def test_no_scalar_exceeds_its_declared_bounds(scenario: dict) -> None:
    m = MeridianModel(scenario=scenario, seed=88213)
    for _ in range(200):
        m.step()
        c = m.state.chain
        for field in ("incident_severity", "insurer_risk", "premium_pressure", "rerouting_level",
                      "port_activity_deficit", "employment_pressure",
                      "household_expectation_pressure", "narrative_attention",
                      "collective_activity", "political_pressure"):
            v = getattr(c, field)
            assert 0.0 <= v <= 1.0, f"{field} out of bounds at tick {m.tick}: {v}"
        for co in m.state.cohorts.values():
            assert 0.0 <= co.economic_concern <= 1.0


def test_repeated_identical_shocks_do_not_accumulate_without_bound(scenario: dict) -> None:
    """Ten identical incidents must not produce ten times the effect."""
    repeated = json.loads(json.dumps(scenario))
    repeated["incidents"] = [
        {"id": f"shock-{i}", "at_tick": i, "severity": 0.85} for i in range(1, 11)
    ]
    m = run(repeated, ticks=30)
    c = m.state.chain
    assert c.incident_severity <= 1.0
    assert c.political_pressure <= 1.0
    assert c.insurer_risk <= 1.0


def test_extended_no_new_input_run_does_not_accumulate_indefinitely(scenario: dict) -> None:
    """After the incident fades, pressure must fall rather than plateau upward forever."""
    m = run(scenario, ticks=HORIZON)
    peak = m.state.chain.political_pressure
    m.run(500)
    late = m.state.chain.political_pressure
    assert late < peak, f"pressure did not recover: peak={peak} late={late}"
    assert late == pytest.approx(0.0, abs=1e-6), f"pressure never returned to rest: {late}"


