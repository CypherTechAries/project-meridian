def test_incident_triggers_the_insurer_mechanism(scenario: dict) -> None:
    assert run(scenario).state.chain.insurer_risk > 0.0


def test_without_an_incident_the_chain_never_begins(baseline_scenario: dict) -> None:
    """The strongest guard against a chain that manufactures its own activity.

    An earlier draft failed this: unscaled turnout jitter made collective activity drift upward
    from zero with no incident at all.
    """
    c = run(baseline_scenario, ticks=40).state.chain
    for field in (
        "incident_severity", "insurer_risk", "premium_pressure", "rerouting_level",
        "port_activity_deficit", "employment_pressure", "household_expectation_pressure",
        "narrative_attention", "collective_activity", "political_pressure",
    ):
        assert getattr(c, field) == 0.0, f"{field} moved with no incident: {getattr(c, field)}"
    assert all(v == "AVAILABLE" for v in c.government_options.values())


def test_incident_is_external_input_not_a_rule(scenario: dict) -> None:
    """The incident must arrive from outside, not be generated by the engine."""
    m = run(scenario, ticks=3)
    incidents = [r for r in m.transition_log if r.type is TransitionType.APPLY_INCIDENT]
    assert incidents, "no incident transition recorded"
    assert all(r.origin.value == "external_input" for r in incidents)


