"""P0.5 — the Kestral Strait cross-tier causal slice.

These prove the chain is REAL: that each declared link carries effect, that removing a link
removes its downstream consequence, that population weighting changes aggregates, and that nothing
accumulates without bound.

The counterfactual tests are the load-bearing ones. A chain that merely produces plausible numbers
proves nothing; a chain where disabling one mechanism demonstrably removes its downstream effect
proves the link exists.
"""

from __future__ import annotations

import json
from pathlib import Path

import pytest

from app.simulation.engine import MeridianModel
from app.simulation.mechanisms import CHAIN, M_HOUSEHOLD, M_INSURER, M_REROUTE
from app.simulation.rules import kestral_v1 as R
from app.simulation.state import canonical_json
from app.simulation.transitions import TransitionType

SCENARIO_PATH = Path(__file__).resolve().parents[2] / "scenarios" / "kestral-strait.json"
HORIZON = 20


@pytest.fixture()
def scenario() -> dict:
    return json.loads(SCENARIO_PATH.read_text())


@pytest.fixture()
def baseline_scenario(scenario: dict) -> dict:
    """Identical scenario with NO incident — the honest control."""
    return {k: v for k, v in scenario.items() if k != "incidents"}


def run(scenario: dict, ticks: int = HORIZON, seed: int = 88213, enabled=None) -> MeridianModel:
    m = MeridianModel(scenario=scenario, seed=seed, enabled_mechanisms=enabled)
    m.run(ticks)
    return m


